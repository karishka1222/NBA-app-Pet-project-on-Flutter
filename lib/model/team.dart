class Team {
  final int id;
  final String conference;
  final String division;
  final String city;
  final String name;
  final String fullName;
  final String abbreviation;

  Team({
    required this.id,
    required this.conference,
    required this.division,
    required this.city,
    required this.name,
    required this.fullName,
    required this.abbreviation,
  });
}
